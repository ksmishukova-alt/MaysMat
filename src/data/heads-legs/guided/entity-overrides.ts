export function entitiesFromOverride(methodTaskId: string): HeadsLegsEntity[] | null {
  const list = ENTITY_LIST_OVERRIDES[methodTaskId];
  if (list?.length) {
    return list.map((label, i) => ({ id: `type${i + 1}`, label }));
  }
  const pair = ENTITY_OVERRIDES[methodTaskId];
  if (!pair) return null;
  return [
    { id: "type1", label: pair[0] },
    { id: "type2", label: pair[1] },
  ];
}
