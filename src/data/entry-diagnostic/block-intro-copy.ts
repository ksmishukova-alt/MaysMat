const COPY_BY_INDEX = new Map(BLOCK_INTRO_COPY.map((item) => [item.blockIndex, item]));

export function getBlockIntroCopy(blockIndex: number): BlockIntroCopy {
  const copy = COPY_BY_INDEX.get(blockIndex);
  if (!copy) {
    throw new Error(`BlockIntro copy not found for theme ${blockIndex}`);
  }
  return copy;
}
