/** Текст мини-примера перед обобщением «одинаковый остаток ⇒ делимость разности» */
export function buildDivisibilityExampleTemplate(modulus: number): string[] {
  if (modulus === 11) {
    return [
      "Например:",
      "",
      "23 при делении на 11 даёт остаток 1.",
      "",
      "34 при делении на 11 тоже даёт остаток 1.",
      "",
      "34 − 23 = 11.",
      "",
      "Значит, разность делится на 11.",
      "",
      "Если два числа имеют одинаковый остаток при делении на 11, то их разность делится на 11.",
    ];
  }

  const a = modulus + 5;
  const b = 2 * modulus + 5;
  return [
    "Например:",
    "",
    `${a} при делении на ${modulus} даёт остаток 5.`,
    "",
    `${b} при делении на ${modulus} тоже даёт остаток 5.`,
    "",
    `${b} − ${a} = ${modulus}.`,
    "",
    `Значит, разность делится на ${modulus}.`,
    "",
    `Если два числа имеют одинаковый остаток при делении на ${modulus}, то их разность делится на ${modulus}.`,
  ];
}
