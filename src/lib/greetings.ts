/** Фразы по дням недели (0=Вс … 6=Сб) */
const BY_WEEKDAY: Partial<Record<number, string>> = {
  1: "Новая неделя — новые звёзды! Daily ждёт в разделе «Задачи».",
  5: "Пятница! Закрой daily и потренируйся в МышМат на выходных.",
  0: "Выходной — отдыхай, а если хочется, МышМат всегда рядом!",
  6: "Суббота! Можно потренировать олимпиадные темы без спешки.",
};

const POOL = [
  "Сегодня отличный день, чтобы решать задачи и собирать звёзды!",
  "Каждая задача — шаг к новой карточке в альбоме!",
  "Попробуй улучшить результат в теме «Головы и ноги»!",
  "МышМат любит, когда думают своей головой — у тебя получится!",
  "Звёзды не ждут — но и спешить некуда, главное — думать!",
  "Сегодня можно заработать звёзды в daily и в олимпиадных темах.",
  "Один верный ход решения лучше десяти догадок — попробуй!",
  "Карта тем открывается постепенно — исследуй её смело!",
];

export function getGreetingSubtitle(): string {
  const now = new Date();
  const weekday = now.getDay();
  if (BY_WEEKDAY[weekday]) return BY_WEEKDAY[weekday]!;

  const dateKey = now.toISOString().slice(0, 10).replace(/-/g, "");
  const index = Number(dateKey) % POOL.length;
  return POOL[index];
}
